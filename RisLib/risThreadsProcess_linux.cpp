void enterProcessHigh()
{
   sched_param param;
   param.sched_priority = sched_get_priority_max(SCHED_FIFO);
   int ret = sched_setscheduler(getpid(), SCHED_FIFO, &param);
   if (ret) printf("sched_setschedparam ERROR %d\n", errno);
}

