//******************************************************************************
//******************************************************************************
//******************************************************************************
// Constructor.

PrintLevel::PrintLevel()
{
   mOutLevel = 0;
   mLogLevel = 3;
}

PrintLevel::PrintLevel(short aOutLevel, short aLogLevel)
{
   mOutLevel = aOutLevel;
   mLogLevel = aLogLevel;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Convert to string for prints.

char* PrintLevel::asString(char* aString)
{
   sprintf(aString,"%d,%d",mOutLevel,mLogLevel);
   return aString;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Read command line command arguments.

void PrintLevel::readArgs(Ris::CmdLineCmd* aCmd)
{
   mOutLevel = aCmd->argInt(1);
   mLogLevel = aCmd->argInt(2);
}

