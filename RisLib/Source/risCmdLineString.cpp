CmdLineString::CmdLineString()
{
}

//******************************************************************************
//******************************************************************************
//******************************************************************************

void CmdLineString::execute (BaseCmdLineExec* aExec, char* aCmdLineString)
{
   // Command
   CmdLineCmd  tCmd;

   // Parse command line string
   tCmd.parseCmdLine(aCmdLineString);

   // Test for exit command
   if(tCmd.isCmd("EXIT"))
   {
      return;
   }

   // Execute the executive with the command
   aExec->execute(&tCmd);

   // Test for bad command
   if(tCmd.isBadCmd())
   {
      Prn::print(0,"INVALID COMMAND\n");
   }
}

