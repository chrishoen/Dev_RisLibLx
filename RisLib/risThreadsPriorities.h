#pragma once

/*==============================================================================
This file provides a set of variables that are used to define default 
thread priorities for base class threads that are defined in RisLib.
==============================================================================*/

//******************************************************************************
//******************************************************************************
//******************************************************************************

namespace Ris
{
namespace Threads
{

//******************************************************************************
//******************************************************************************
//******************************************************************************
// This is a struct that encapsulates thread single processor and priority.

class Priority
{
public:

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Members.

   // Thread single processor number and priority.
   short mProcessor;
   short mPriority;

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Methods.

   // Constructor.
   Priority();
   Priority(short aProcessor, short aPriority);

};

//******************************************************************************
//******************************************************************************
//******************************************************************************
// This class provides provides a set of variables that are used to define default 
// thread priorities for base class threads that are defined in RisLib.

class Priorities
{
public:

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Members.

   Priority mLow;
   Priority mNormal;
   Priority mHigh;

   Priority mQCall;
   Priority mShort;
   Priority mLong;

   Priority mUdp;
   Priority mTcpServer;
   Priority mTcpClient;
   Priority mSerial;

   Priority mTsPrint;

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Methods.

   // Constructor.
   Priorities();
};

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Global singular instance.

#ifdef _RISTHREADSPRIORITIES_CPP_
          Priorities gPriorities;
#else
   extern Priorities gPriorities;
#endif

//******************************************************************************
//******************************************************************************
//******************************************************************************
}//namespace
}//namespace

