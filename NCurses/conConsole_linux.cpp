// Run test loop.

void Console::doTestLoop1()
{
   Prn::print(Prn::View11, "Console::doTestLoop1****************************");

   while (true)
   {
      int tChar = readOne();
      if (tChar == ' ')
      {
         Prn::print(Prn::View11, "****************************");
      }
      else
      {
         Prn::print(Prn::View11, " %3d", tChar);
      }

      if (tChar == 'z')
      {
         Prn::print(Prn::View11, "exit");
         break;
      }
   }
}

